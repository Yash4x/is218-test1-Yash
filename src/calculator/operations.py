def add(a, b):
    """
    Add two numbers and return the result.

    Args:
        a: First number
        b: Second number

    Returns:
        The sum of a and b
    """
    return a + b

def subtract(a, b):
    """
    Subtract b from a and return the result.

    Args:
        a: Number to subtract from
        b: Number to subtract

    Returns:
        The difference (a - b)
    """
    return a - b